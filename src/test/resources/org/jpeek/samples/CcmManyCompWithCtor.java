public class CcmManyCompWithCtor {

    String name1;

    String name2;

    String name3;

    String name4;

    String name5;

    public CcmManyComponentWithCtor() {
        name1 = "one";
        name2 = "two";
        name3 = "three";
        name4 = "four";
        name5 = "five";
    }

    public void one() {
        name1 = "1";
    }

    public void two() {
        name2 = "2";
    }

    public void three() {
        name3 = "3";
    }

    public void four() {
        name4 = "4";
    }

    public void five() {
        name5 = "5";
    }

}